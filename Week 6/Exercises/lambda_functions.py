# Example 1:

def square(n):
    return n*n

print(square(7))

# Using lambda function

square = lambda n : n*n
print(square(7))

# Example 2:

def sum(a,b,c):
    return a+b+c

print(sum(5,10,7))

# Using lambda function

sum = lambda a,b,c : a+b+c
print(sum(5,10,7))

divide = lambda a,b : a/b
print(divide(10,2))
